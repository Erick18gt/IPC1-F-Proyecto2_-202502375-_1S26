    private void btnEditarEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarEstudianteActionPerformed
      UsuarioControler u = new UsuarioControler();
     String codigo = JOptionPane.showInputDialog("Ingrese código del estudiante");
     if(codigo == null) return;
     Usuario usuario = u.buscarPorCodigo(codigo); 
     if(usuario == null){
      JOptionPane.showMessageDialog(null, "Estudiante no encontrado");
     return;
  }


    if(!(usuario instanceof estudiante)){
    JOptionPane.showMessageDialog(null, "El código no pertenece a un estudiante");
    return;
    }
    estudiante e = (estudiante) usuario;
    String nuevoNombre = JOptionPane.showInputDialog("Nuevo nombre", e.getNombre());
    if(nuevoNombre == null) return;
    String nuevoApellido = JOptionPane.showInputDialog("Nuevo apellido", e.getApellido());
    if(nuevoApellido == null) return;
    String nuevaPass = JOptionPane.showInputDialog("Nueva contraseña", e.getContraseña());
    if(nuevaPass == null) return;
    e.setNombre(nuevoNombre);
    e.setApellido(nuevoApellido);
    e.setContraseña(nuevaPass);
    UsuarioControler.guardarUsuarios();
    JOptionPane.showMessageDialog(null, "Estudiante actualizado");

      
    }//GEN-LAST:event_btnEditarEstudianteActionPerformed

