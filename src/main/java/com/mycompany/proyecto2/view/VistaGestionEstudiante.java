    private void btnBuscarEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEstudianteActionPerformed
     UsuarioControler u = new UsuarioControler();
     String codigo = JOptionPane.showInputDialog("Ingrese código del estudiante");

     if(codigo == null) return;

     Usuario usuario = u.buscarPorCodigo(codigo);


      if(usuario == null){
      JOptionPane.showMessageDialog(null, "Estudiante no encontrado");
     return;
  }


if(!(usuario instanceof estudiante)){
    JOptionPane.showMessageDialog(null, "El código no pertenece a un estudiante");
    return;
}


estudiante e = (estudiante) usuario;


String info = "Código: " + e.getCodigo() +
              "\nContraseña: " + e.getContraseña() +
              "\nNombre: " + e.getNombre() +
              "\nApellido: " + e.getApellido() +
              "\nFecha Nacimiento: " + e.getFechaNacimiento() +
              "\nGénero: " + e.getGenero();

JOptionPane.showMessageDialog(null, info);
    }//GEN-LAST:event_btnBuscarEstudianteActionPerformed

